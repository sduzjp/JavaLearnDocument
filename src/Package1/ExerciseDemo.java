/**
 * 创建一个锻炼类
 * 成员变量：姓名，年龄，性别
 * 成员方法：打球，游泳，跑步
 */
public class ExerciseDemo {
    //成员变量
    //姓名
    public String name;
    //年龄
    public int age;
    //性别
    public String gender;

    //成员方法
    public void basketball( ) {
        System.out.println( "篮球" );
    }

    public void swimming( ) {
        System.out.println( "游泳" );
    }

    public void running( ) {
        System.out.println( "跑步" );
    }

}
